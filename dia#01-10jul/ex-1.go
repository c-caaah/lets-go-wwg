// Utilizando a palavra reservada var declare uma variável numérica do tipo int.
// Atribua um valor a essa variável.
// Printe na tela o seu valor.
// Repita para 3 variáveis diferentes..

package main

import (
	"fmt"
)

func main() {
	var num1 int
	num1 = 7
	fmt.Println(num1)

	var num2 int
	num2 = 9
	fmt.Println(num2)

	var num3 int
	num3 = 55
	fmt.Println(num3)

	var num4 int
	num4 = 22
	fmt.Println(num4)

}
